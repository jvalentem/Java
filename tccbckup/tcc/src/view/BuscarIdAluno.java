    public void buscar(){
        int id = Integer.parseInt(input_id.getText());
        Aluno aluno =  new AlunoDAO().buscarPorID(id);
        if(aluno.getNome() == null){
            statusMessage.setText("Aluno n√£o encontrado!");
            return;
        }
        EdicaoAluno telaEdicaoAluno = new EdicaoAluno(aluno);
        telaEdicaoAluno.setVisible(true);
        this.setVisible(false);
        telaEdicaoAluno.addWindowListener(new WindowAdapter(){
            @Override 
            public void windowClosed(WindowEvent e){
                dispose();
            }
        });
        
    }
