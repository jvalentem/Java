    private void deletarAluno(){
        try{
            
        int idAluno = Integer.parseInt(input_id.getText());
        Aluno aluno = new AlunoDAO().buscarPorID(idAluno);
        System.out.println("ID do aluno: " + aluno.getAlunoID());
        
        if(!aluno.getNome().isEmpty()){
        new AlunoDAO().excluir(aluno);
        this.dispose();
        return;
        }
        statusMessage.setForeground(Color.red);
        statusMessage.setText("Aluno n√£o encontrado!");
        
        }catch(Exception e){
            statusMessage.setForeground(Color.red);
            statusMessage.setText("Algo deu errado, verifique o ID e tente novamente");
            System.out.println(e.toString());
        }
       
    }
